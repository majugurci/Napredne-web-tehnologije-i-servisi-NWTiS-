package org.foi.nwtis.majugurci.web.kontrole;

import java.util.Date;

/**
 *
 * @author Mario
 */
public class Korisnik {

    private String korisnickoIme = null;
    private int grupa;
    private String email;
    private float sredstva;
    private Date vrijemeRegistracije;
    
    public Korisnik() {
        
    }

    public Korisnik(String korisnickoIme, int grupa, String email, float sredstva, Date vrijemeRegistracije) {
        this.korisnickoIme = korisnickoIme;
        this.grupa = grupa;
        this.email = email;
        this.sredstva = sredstva;
        this.vrijemeRegistracije = vrijemeRegistracije;
    }

    public String getKorisnickoIme() {
        return korisnickoIme;
    }

    public void setKorisnickoIme(String korisnickoIme) {
        this.korisnickoIme = korisnickoIme;
    }

    public int getGrupa() {
        return grupa;
    }

    public void setGrupa(int grupa) {
        this.grupa = grupa;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public float getSredstva() {
        return sredstva;
    }

    public void setSredstva(float sredstva) {
        this.sredstva = sredstva;
    }

    public Date getVrijemeRegistracije() {
        return vrijemeRegistracije;
    }

    public void setVrijemeRegistracije(Date vrijemeRegistracije) {
        this.vrijemeRegistracije = vrijemeRegistracije;
    }
    
    
}
